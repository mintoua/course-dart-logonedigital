void main(){
  print("Bienvenue dans la formation dev mobile avec flutter !!!");
  print("Nous somme sur la branch structures itératives");

  //Boucle do  while
  int i = 0;
  do {
    print("Itération $i");
    i++;
  } while (i < 5);

  //Boucle for
  for (int j = 0; j < 5; j++) {
    print("Itération $j");
  }
  
  //Boucle while
  int k = 0;
  while (k < 5) {
    print("Itération $k");
    k++;
  }

}
